def zh_formatter(weekday: str):
    if weekday == "1":
        return "星期一"
    if weekday == "2":
        return "星期二"
    if weekday == "3":
        return "星期三"
    if weekday == "4":
        return "星期四"
    if weekday == "5":
        return "星期五"
    if weekday == "6":
        return "星期六"
    if weekday == "7":
        return "星期日"